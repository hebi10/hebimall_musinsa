export const getProductColorValue = (colorName: string): string => {
  const normalizedColor = colorName.trim().toLowerCase();

  if (!normalizedColor) {
    return '#cccccc';
  }

  if (EXACT_COLOR_MAP[normalizedColor]) {
    return EXACT_COLOR_MAP[normalizedColor];
  }

  const partialMatch = PARTIAL_COLOR_MAP.find(([name]) => normalizedColor.includes(name));
  return partialMatch?.[1] ?? '#cccccc';
};
